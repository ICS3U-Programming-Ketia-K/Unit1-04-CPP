// This program calculates and displays the area and
// perimeter of a rectangle with dimensions 7cm X 5cm.
#include <iostream>

int main() {
  std::cout << "For a rectangle that has dimensions\n";
  std::cout << "length 7cm and width 5cm:\n";
  std::cout << "\n";
  std::cout << "Area = " << 7 * 5 << " cm^2\n";
  std::cout << "Perimeter = " << 2* (7 + 5) << " cm\n";
}
