#include <iostream>
#include <cmath>

int main() {
  std::cout << "6 + 2 = " << 6+2 << std::endl;
  std::cout << "7 - 5 = " << 7-5 << std::endl;
  std::cout << "10 / 5 = " << 10/5 << std::endl;
  std::cout << "8 - 2 / 2 = " << 8-2/2 << std::endl;
  std::cout << "4^3 = " << pow(4, 3) << std::endl;
  std::cout << "6 / 3 = " << float(6)/float(3) << std::endl;
}
